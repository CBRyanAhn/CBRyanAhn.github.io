<p> 
<b> Abstract </b><br>
We propose a method to expand the visual coverage of training sets that consist of a small number of labeled examples using learned attributes. Our optimization formulation discovers category specific attributes as well as the images that have high con.dence in terms of the attributes. In addition, we propose a method to stably capture example-specific attributes for a small sized training set. Our method adds images to a category from a large unlabeled image pool, and leads to signi.cant improvement in category recognition accuracy evaluated on a large-scale dataset, ImageNet.
</p>

